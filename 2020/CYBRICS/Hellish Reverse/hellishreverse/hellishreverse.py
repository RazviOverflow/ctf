#!/usr/bin/python3
import hashlib, re
from Crypto.Cipher import AES, Blowfish, ARC4  # pip3 install pycryptodome

print("I'm thinking of a flag!")
print("format: cybrics{...................................}")
print()
guess = input("Your guess? ")

if not re.match(r'^cybrics\{...................................\}$', guess):
    print("Oh come on, the format! ↑↑↑")
    exit()

data = guess.encode('utf-8')
data = hashlib.sha256(data).digest()
data = hashlib.sha1(data).digest()
data = hashlib.md5(data).digest()
data = AES.new(data, AES.MODE_ECB).encrypt(data)
data = Blowfish.new(data, Blowfish.MODE_ECB).encrypt(data)
data = ARC4.new(data).encrypt(data)
data = Blowfish.new(data, Blowfish.MODE_ECB).encrypt(data)
data = AES.new(data, AES.MODE_ECB).encrypt(data)
data = hashlib.md5(data).digest()
data = hashlib.sha1(data).digest()
data = hashlib.sha256(data).digest()

if data.hex() != "f9467c35f11f6c2ffc16c7ac026efaacf262e428409c7595f285f933c84d2ea1":
    print("Surprisingly, it's incorrect!")
else:
    print("Surprisingly, it's correct!")

exit()





































































































































































































































































































